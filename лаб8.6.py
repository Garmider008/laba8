import numpy as np
import random
m1=np.array(range(1,15))  #Створимо масив, для перевірки правильності нашого пошуку.
print(m1)
x=int(input('Введіть елемент для пошуку'))#Якщо користувач введе більше 14, то елемент знайдений не буде
R=len(m1)-1 #Конець масиву
L,count,k=0,0,0, #Початок масиву, лічильник для порівнянь, і змінна для позиції числа
flag=False
while L<=R and not flag :
    k=(L+R)//2 #Припустимо, якщо ввести 9, При першій ітерації, к = 6, при другій к = 10 при третин к = 8
    count+=1   #лічильник ітерацій
    if m1[k]==x:
        flag=True
    elif m1[k]<x:
        L=k+1
    else:
        R=k-1
if not flag:
    print('Елемент не знайдено')
else:
    print(f'Елемент знайдений на позиції {k} за {count} порівнянь')
#Робота з рандомних значеннями
